import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const configPath = path.resolve(__dirname, "../src/shared/styles/typescale.config.json");
const outputPath = path.resolve(__dirname, "../src/shared/styles/typescale.generated.css");

const config = JSON.parse(await readFile(configPath, "utf8"));

const rem = (step) => `${(config.basePx * config.ratio ** step / 16).toFixed(3)}rem`;
const px = (step) => `${(config.basePx * config.ratio ** step).toFixed(2)}px`;
const pt = (step) => `${(config.basePx * config.ratio ** step * 0.75).toFixed(2)}pt`;

const rows = [
  ["h1", config.steps.h1],
  ["h2", config.steps.h2],
  ["h3", config.steps.h3],
  ["h4", config.steps.h4],
  ["h5", config.steps.h5],
  ["h6", config.steps.h6],
  ["body", config.steps.body],
  ["caption", config.steps.caption],
  ["micro", config.steps.micro],
];

const toVarName = (token) => `--ui-font-size-${token}`;

const css = `/* Generated by npm run typescale.
   Source: src/shared/styles/typescale.config.json
   Base: ${config.basePx}px
   Ratio: ${config.ratio}

   Token     rem       px        pt
${rows
  .map(([name, step]) => `   ${name.padEnd(8)} ${rem(step).padEnd(9)} ${px(step).padEnd(9)} ${pt(step)}`)
  .join("\n")}
*/

:root {
  --ui-font-family-base: ${config.fontFamilyBase};
  --ui-color-text: ${config.colors.primary};
  --ui-color-text-muted: ${config.colors.secondary};
  --ui-color-text-soft: ${config.colors.soft};

  ${toVarName("h1")}: ${rem(config.steps.h1)};
  ${toVarName("h2")}: ${rem(config.steps.h2)};
  ${toVarName("h3")}: ${rem(config.steps.h3)};
  ${toVarName("h4")}: ${rem(config.steps.h4)};
  ${toVarName("h5")}: ${rem(config.steps.h5)};
  ${toVarName("h6")}: ${rem(config.steps.h6)};
  --ui-font-size-body-lg: ${rem(config.steps.bodyLg)};
  ${toVarName("body")}: ${rem(config.steps.body)};
  --ui-font-size-caption: ${rem(config.steps.caption)};
  --ui-font-size-micro: ${rem(config.steps.micro)};

  --ui-line-height-tight: ${config.lineHeights.tight};
  --ui-line-height-heading: ${config.lineHeights.heading};
  --ui-line-height-body: ${config.lineHeights.body};
}
`;

await writeFile(outputPath, css, "utf8");
globalThis.console.log(`Generated ${path.relative(process.cwd(), outputPath)}`);

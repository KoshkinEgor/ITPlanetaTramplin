import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const configPath = path.resolve(__dirname, "../src/shared/styles/typescale.config.json");
const outputPath = path.resolve(__dirname, "../src/shared/styles/typescale.generated.css");

const config = JSON.parse(await readFile(configPath, "utf8"));

const STYLE_ORDER = ["display", "h1", "h2", "h3", "h4", "h5", "h6", "bodyLg", "body", "caption", "small", "micro", "overline"];
const STYLE_LABELS = {
  bodyLg: "body-lg",
};

const getTokenName = (styleName) => STYLE_LABELS[styleName] ?? styleName;

const styleRows = STYLE_ORDER.map((styleName) => {
  const style = config.styles[styleName];
  return [
    styleName.padEnd(8),
    style.desktop.fontSize.padEnd(9),
    style.mobile.fontSize,
  ].join(" ");
});

const renderStyleTokens = (viewport, indent = "") =>
  STYLE_ORDER.flatMap((styleName) => {
    const tokenName = getTokenName(styleName);
    const style = config.styles[styleName][viewport];
    return [
      `${indent}--ui-font-size-${tokenName}: ${style.fontSize};`,
      `${indent}--ui-line-height-${tokenName}: ${style.lineHeight};`,
      `${indent}--ui-letter-spacing-${tokenName}: ${style.letterSpacing};`,
      `${indent}--ui-font-weight-${tokenName}: ${style.fontWeight};`,
    ];
  }).join("\n");

const css = `/* Generated by npm run typescale.
   Source: src/shared/styles/typescale.config.json

   Token     desktop   mobile
${styleRows.map((row) => `   ${row}`).join("\n")}
*/

:root {
  --ui-font-family-base: ${config.fontFamilyBase};

${renderStyleTokens("desktop", "  ")}
  --ui-font-weight-heading: ${config.fontWeights.heading};
  --ui-font-weight-regular: ${config.fontWeights.regular};
  --ui-font-weight-strong: ${config.fontWeights.strong};
  --ui-line-height-tight: var(--ui-line-height-h1);
  --ui-line-height-heading: var(--ui-line-height-h2);
}

@media (max-width: 767px) {
  :root {
${renderStyleTokens("mobile", "    ")}
  }
}
`;

await writeFile(outputPath, css, "utf8");
globalThis.console.log(`Generated ${path.relative(process.cwd(), outputPath)}`);
